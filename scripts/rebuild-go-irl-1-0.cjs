#!/usr/bin/env node
/*
  GO IRL 1.0 clean rebuild helper.

  Purpose:
  - Create a safe clean-copy folder named "GO IRL 1.0" from the current repo.
  - Copy only confirmed runtime/config/docs directories and root files.
  - Exclude local caches, build output, secrets, Supabase temp state, npm/yarn artifacts.
  - Validate with pnpm only.

  Usage from repo root:
    node scripts/rebuild-go-irl-1-0.cjs
    node scripts/rebuild-go-irl-1-0.cjs --force
    node scripts/rebuild-go-irl-1-0.cjs --target "C:\\Users\\lenovo\\Documents\\GO IRL 1.0"
    node scripts/rebuild-go-irl-1-0.cjs --skip-install
    node scripts/rebuild-go-irl-1-0.cjs --skip-validation
*/

const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');

const repoRoot = process.cwd();
const args = process.argv.slice(2);

const getArg = (name) => {
  const index = args.indexOf(name);
  if (index === -1) return null;
  return args[index + 1] || null;
};

const hasArg = (name) => args.includes(name);

const targetArg = getArg('--target');
const targetRoot = path.resolve(targetArg || path.join(repoRoot, '..', 'GO IRL 1.0'));
const force = hasArg('--force');
const skipInstall = hasArg('--skip-install');
const skipValidation = hasArg('--skip-validation');

const allowDirs = [
  '.github',
  'docs',
  'project-audit',
  'public',
  'scripts',
  'src',
  'supabase',
];

const allowRootFiles = [
  '.env.example',
  '.gitignore',
  '.npmignore',
  '.vercelignore',
  'CHANGELOG.md',
  'README.md',
  'RELEASE_NOTES.md',
  'ROADMAP.md',
  'eslint.config.js',
  'index.html',
  'package.json',
  'pnpm-lock.yaml',
  'tsconfig.app.json',
  'tsconfig.json',
  'tsconfig.node.json',
  'vercel.json',
  'vite.config.ts',
];

const blockedNames = new Set([
  '.git',
  '.env',
  '.env.local',
  '.env.development.local',
  '.env.production.local',
  'node_modules',
  'dist',
  '.vite',
  '.turbo',
  '.next',
  'coverage',
  'package-lock.json',
  'yarn.lock',
  'npm-debug.log',
  'pnpm-debug.log',
  'yarn-debug.log',
  'yarn-error.log',
]);

const blockedRelative = new Set([
  'supabase/.temp',
]);

const blockedExtensions = [
  '.tsbuildinfo',
  '.bak',
  '.tmp',
  '.log',
];

const rootReviewArtifacts = new Set([
  'snapshot.txt',
  'deepseek_powershell_20260705_284627.ps1',
  'ui-cleanup-v2-report.txt',
  'CHECKLIST.md',
  'GO_IRL_DOCUMENTATION.md',
  'PATCH_REPORT.md',
  'SETUP.md',
  'SETUP_RU.md',
  '.replit',
]);

const toPosix = (value) => value.split(path.sep).join('/');

const shouldSkip = (absolutePath) => {
  const name = path.basename(absolutePath);
  const relative = toPosix(path.relative(repoRoot, absolutePath));

  if (blockedNames.has(name)) return true;
  if (blockedRelative.has(relative)) return true;
  if ([...blockedRelative].some((blocked) => relative.startsWith(`${blocked}/`))) return true;
  if (blockedExtensions.some((extension) => name.endsWith(extension))) return true;

  return false;
};

const ensureInsideRepo = () => {
  const packagePath = path.join(repoRoot, 'package.json');
  const lockPath = path.join(repoRoot, 'pnpm-lock.yaml');

  if (!fs.existsSync(packagePath) || !fs.existsSync(lockPath)) {
    throw new Error('Run this script from the GO IRL repo root. package.json or pnpm-lock.yaml was not found.');
  }
};

const removeDir = (dir) => {
  if (!fs.existsSync(dir)) return;
  fs.rmSync(dir, { recursive: true, force: true });
};

const copyFile = (source, destination) => {
  fs.mkdirSync(path.dirname(destination), { recursive: true });
  fs.copyFileSync(source, destination);
};

const copyDir = (sourceDir, destinationDir) => {
  if (!fs.existsSync(sourceDir)) return;
  if (shouldSkip(sourceDir)) return;

  const stat = fs.statSync(sourceDir);
  if (!stat.isDirectory()) return;

  fs.mkdirSync(destinationDir, { recursive: true });

  for (const entry of fs.readdirSync(sourceDir, { withFileTypes: true })) {
    const sourcePath = path.join(sourceDir, entry.name);
    const destinationPath = path.join(destinationDir, entry.name);

    if (shouldSkip(sourcePath)) continue;

    if (entry.isDirectory()) {
      copyDir(sourcePath, destinationPath);
    } else if (entry.isFile()) {
      copyFile(sourcePath, destinationPath);
    }
  }
};

const writeRebuildNotes = () => {
  const notes = [
    '# GO IRL 1.0 Clean Copy',
    '',
    'Generated by `scripts/rebuild-go-irl-1-0.cjs`.',
    '',
    '## What was copied',
    '',
    '- Runtime source: `src/`',
    '- Static assets: `public/`',
    '- Supabase project files: `supabase/` except `supabase/.temp`',
    '- CI/deploy/config files required for Vite + pnpm + Vercel',
    '- Documentation folders: `docs/`, `project-audit/`',
    '',
    '## What was intentionally not copied',
    '',
    '- `.env*` secrets except `.env.example`',
    '- `node_modules/`',
    '- `dist/`',
    '- npm/yarn lock files',
    '- local snapshots/reports from the audit review category',
    '- Supabase local CLI temp state: `supabase/.temp`',
    '',
    '## Validation commands',
    '',
    '```powershell',
    'pnpm run lint',
    'pnpm run build',
    'pnpm run test',
    'git status --short',
    '```',
    '',
  ].join('\n');

  fs.writeFileSync(path.join(targetRoot, 'GO_IRL_1_0_REBUILD_NOTES.md'), notes, 'utf8');
};

const run = (command, commandArgs, cwd) => {
  console.log(`\n> ${command} ${commandArgs.join(' ')}`);
  const result = spawnSync(command, commandArgs, {
    cwd,
    stdio: 'inherit',
    shell: process.platform === 'win32',
  });

  if (result.status !== 0) {
    throw new Error(`Command failed: ${command} ${commandArgs.join(' ')}`);
  }
};

const main = () => {
  ensureInsideRepo();

  if (fs.existsSync(targetRoot)) {
    if (!force) {
      throw new Error(`Target already exists: ${targetRoot}. Re-run with --force to replace it.`);
    }
    removeDir(targetRoot);
  }

  fs.mkdirSync(targetRoot, { recursive: true });

  for (const dir of allowDirs) {
    copyDir(path.join(repoRoot, dir), path.join(targetRoot, dir));
  }

  for (const file of allowRootFiles) {
    const source = path.join(repoRoot, file);
    const destination = path.join(targetRoot, file);
    if (fs.existsSync(source) && !rootReviewArtifacts.has(file) && !shouldSkip(source)) {
      copyFile(source, destination);
    }
  }

  writeRebuildNotes();

  console.log('\nGO IRL 1.0 clean copy created:');
  console.log(targetRoot);

  if (!skipInstall) {
    run('pnpm', ['install', '--frozen-lockfile'], targetRoot);
  }

  if (!skipValidation) {
    run('pnpm', ['run', 'lint'], targetRoot);
    run('pnpm', ['run', 'build'], targetRoot);
    run('pnpm', ['run', 'test'], targetRoot);
  }

  console.log('\nDone. Final check:');
  console.log(`cd "${targetRoot}"`);
  console.log('git status --short');
};

try {
  main();
} catch (error) {
  console.error('\nGO IRL 1.0 rebuild failed.');
  console.error(error && error.message ? error.message : error);
  process.exit(1);
}
